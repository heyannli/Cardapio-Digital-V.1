// exibir a página inicial
function showHomePage() {
    document.getElementById('home-page').style.display = 'block';
    document.getElementById('category-page').style.display = 'none';
}

// exibir categoria específica
function showCategory(categoryId) {
    document.getElementById('home-page').style.display = 'none';

    const categoryPage = document.getElementById('category-page');
    categoryPage.style.display = 'block';

    const titles = {
        bebidas: "<img src='imagens/bebidas.png'> Bebidas",
        cervejas: "<img src='imagens/cerveja.png'> Cervejas",
        batidas: "<img src='imagens/batidas.png'> Batidas",
        doses: "<img src='imagens/dose.png'> Doses",
        combos: "<img src='imagens/combo.png'> Combos",
        drinks: "<img src='imagens/drink.png'> Drinks",
        vinhos: "<img src='imagens/vinho.png'> Vinhos",
        porcoes: "<img src='imagens/porcoes.png'> Porções",
    };

    document.getElementById('category-title').innerHTML = titles[categoryId];

    const menuItems = document.getElementById('menu-items');
        menuItems.innerHTML = '';

    const items = menuData[categoryId];
    items.forEach(item => {
        const itemElement = document.createElement('div');
        itemElement.className = 'menu-item';
        itemElement.innerHTML = `
            <div class="item-header">
                <span class="item-name">${item.name}</span>
                <span class="item-price">${item.price}</span>
            </div>
            <p class="item-description">${item.description}</p>
        `;
        menuItems.appendChild(itemElement);
    });
}

showHomePage();